package ex11_Increment_operators;

public class Lab47_increment_example02 {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(a++ + a);  // A -> a++ , B -> a
        // A + B
        // A ->  a++ -> 10 -> a = 11
        // B -> a -> 11
        // A  + b -> 10 + 11
    }
}
