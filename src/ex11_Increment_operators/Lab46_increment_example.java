package ex11_Increment_operators;

public class Lab46_increment_example {
    public static void main(String[] args) {
        int a = 18;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);
        System.out.println(a++);

        // method
        // line no . / Exp(result) / a
        // 5 / NA / 18
        // 6 / 19  / 19





    }
}
