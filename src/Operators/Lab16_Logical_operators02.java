package Operators;

public class Lab16_Logical_operators02 {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(a);
        boolean b = true;
        System.out.println(b);
        boolean c = false;
        System.out.println(c);
        boolean c2 = true || false;
        System.out.println(c2);

    }
}
